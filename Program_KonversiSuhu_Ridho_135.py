# mengkonversi suhu Celcius ke Fahrenheit

suhu = int(input("Masukan Nilai Suhu Celcius Yang Anda Ingin Konversi Menjadi Farenheit :"))
conv_suhu = 9 * suhu / 5+32

print('----------------------------------------------------------------')
print(f'Hasil Konversi Suhu {suhu} Celcius adalah {conv_suhu} farenheit')
print('----------------------------------------------------------------')




# mengkonversi suhu Kelvin menjadi Celcius

Kelvin = int(input('Masukan Nilai Suhu Kelvin Yang Ingin Anda Konversi Ke Celcius :'))
Konv_suhu = Kelvin - 273
print('----------------------------------------------------------------')
print(f'Hasil Konversi Suhu {Kelvin} Kelvin adalah {Konv_suhu} Celcius')
print('----------------------------------------------------------------')
